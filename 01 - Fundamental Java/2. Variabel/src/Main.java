public class Main {
    public static void main(String[] args) {
        String nama = "elang";
        int umur = 19;
        String kota = "bogor";
        double tinggi = 175.5;
        boolean mahasiswa = true;

        System.out.println(nama);
        System.out.println(umur);
        System.out.println(kota);
        System.out.println(tinggi);
        System.out.println(mahasiswa);
    }
}

